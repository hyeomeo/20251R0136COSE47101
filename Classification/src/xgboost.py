import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import OneHotEncoder
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from xgboost import XGBRegressor

# ë°ì´í„° ë¶ˆëŸ¬ì˜¤ê¸°
df = pd.read_csv("preprocessed_with_cluster_ALL.csv")

# REGION ì œê±°
if "REGION" in df.columns:
    df = df.drop(columns=["REGION"])

# ì£¼ìš” ê²°ì¸¡ ì œê±°
df = df.dropna(subset=[
    "DGSTFN", "VISIT_AREA_NM", "MAIN_TRAVEL_MONTH", "MVMN_NM",
    "REVISIT_INTENTION", "RCMDTN_INTENTION", "RELATION_TYPE",
    "VISIT_AREA_TYPE_CD"
])

# íƒ€ê²Ÿê³¼ í”¼ì²˜ ë¶„ë¦¬
X_reg = df.drop(columns=[
    "DGSTFN", "label", "TRAVELER_ID", "TRAVEL_ID",
    "REVISIT_INTENTION", "RCMDTN_INTENTION",
    "TRAVEL_START_YMD", "TRAVEL_END_YMD", "CLUSTER"
], errors="ignore")

y_reg = df["DGSTFN"]

# ë²”ì£¼í˜•/ìˆ˜ì¹˜í˜• ë³€ìˆ˜ ë¶„ë¦¬
cat_features = [
    "VISIT_AREA_NM", "MAIN_TRAVEL_MONTH", "TRAVEL_STATUS_ACCOMPANY",
    "RELATION_TYPE", "MVMN_NM", "GENDER", "AGE_GRP", "VISIT_AREA_TYPE_CD"
]

num_features = [
    "TRAVEL_STYL_1", "TRAVEL_STYL_3", "TRAVEL_STYL_5",
    "TRAVEL_STYL_6", "TRAVEL_STYL_7", "TRAVEL_STYL_8"
]

# ColumnTransformer ì„¤ì •
preprocessor = ColumnTransformer([
    ("cat", OneHotEncoder(handle_unknown="ignore"), cat_features),
    ("num", "passthrough", num_features)
], remainder="drop")  # ë‚˜ë¨¸ì§€ëŠ” ì œê±°

# ì „ì²´ íŒŒì´í”„ë¼ì¸ êµ¬ì„±
reg_pipeline = Pipeline([
    ("preprocessor", preprocessor),
    ("regressor", XGBRegressor(
        objective="reg:squarederror",
        n_estimators=100,
        max_depth=5,
        learning_rate=0.1,
        random_state=42
    ))
])

# í•™ìŠµ/í…ŒìŠ¤íŠ¸ ë¶„í• 
X_train, X_test, y_train, y_test = train_test_split(
    X_reg, y_reg, test_size=0.3, random_state=42
)

# í•™ìŠµ ë° ì˜ˆì¸¡
reg_pipeline.fit(X_train, y_train)
y_pred = reg_pipeline.predict(X_test)

# í‰ê°€ ê²°ê³¼ ì¶œë ¥
print("\nğŸ“Š XGBoost Regression (with TRAVEL_STYLE)")
print("âœ… MAE:", round(mean_absolute_error(y_test, y_pred), 4))
print("âœ… MSE:", round(mean_squared_error(y_test, y_pred), 4))
print("âœ… RÂ² :", round(r2_score(y_test, y_pred), 4))